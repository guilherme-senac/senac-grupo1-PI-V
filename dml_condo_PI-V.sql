    condo.morador m
JOIN 
    condo.condominio c ON c.Id = (m.Id % (SELECT COUNT(*) FROM condo.condominio) + 1) -- Distribui cada morador em um nico condomnio
ORDER BY 
    c.Id, m.Id;
	
