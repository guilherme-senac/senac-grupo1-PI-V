    m.Id AS moradorid,
    CONCAT('BLOCO', LPAD(((ROW_NUMBER() OVER (ORDER BY m.Id) - 1) % 5) + 1, 2, '0')) AS NMBloco, 
    ((ROW_NUMBER() OVER (ORDER BY m.Id) - 1) % 5) + 1 AS NrBloco, 
    c.Id AS CondominioID,
    2 AS tpunidadeid -- tipomoradorid fixo como 2
